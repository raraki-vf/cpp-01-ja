#include "math_operations.h"

namespace MathOps
{
    float addition (float x, float y){
        return x + y;
    }

    float subtraction (float x, float y){
        return x - y;
    }

    float multiplication (float x, float y){
        return x * y;
    }

    float division (float x, float y){
        return x / y;
    }

} // namespace MathOps
